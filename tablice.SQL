CREATE TABLE DJELATNIK ( 
 ID_DJELATNIK CHAR(6), 
 	IME_DJELATNIK NVARCHAR(20) NOT NULL, 
 	PREZ_DJELATNIK NVARCHAR(20) NOT NULL, 
 	PLACA NUMERIC(7, 2) CHECK(PLACA > 0), 
 	DAT_ZAPOSLENJA SMALLDATETIME NOT NULL, 
 	CONSTRAINT PK_DJELATNIK PRIMARY KEY(ID_DJELATNIK)
);

CREATE TABLE KUPAC ( 
 ID_KUPCA CHAR(6), 
 IME_KUPAC NVARCHAR(20) NOT NULL, 
 PREZIME_KUPAC NVARCHAR(20) NOT NULL,
 ADRESA NVARCHAR(30) NOT NULL, 
 TEL_BR NVARCHAR(10),
 CONSTRAINT PK_KUPAC PRIMARY KEY(ID_KUPCA)
);

CREATE TABLE NARUDZBA ( 
 ID_NAR CHAR(6), 
 ID_KUPAC CHAR(6), 
 ID_DJELATNIK CHAR(6), 
 DAT_NAR SMALLDATETIME NOT NULL,  
 CONSTRAINT PK_NAR PRIMARY KEY(ID_NAR), 
 CONSTRAINT FK_NAR_DJELATNIK FOREIGN KEY(ID_DJELATNIK) REFERENCES DJELATNIK(ID_DJELATNIK), 
 CONSTRAINT FK_NAR_KUPAC FOREIGN KEY(ID_KUPAC) REFERENCES KUPAC(ID_KUPCA)
);

CREATE TABLE OPREMA ( 
 ID_OPR CHAR(6), 
 VRSTA_OPR CHAR(10),
 STANJE INT,
 CONSTRAINT PK_OPR PRIMARY KEY(ID_OPR)
);
